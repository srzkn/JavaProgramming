package day05_Concatenation;

public class variables_Practice {
    public static void main(String[] args) {
        byte num1=100;
        short num2=10000;
        double num3=3.5;
        float num4=2.5f;
        long num5=9_999_999_999L;
        int num6=850000;
        System.out.println(num5+" > "+num6+" > "+num2+" > "+num1+" > "+num3+" > "+num4);
        System.out.println("num5 = " + num5);
    }
}
