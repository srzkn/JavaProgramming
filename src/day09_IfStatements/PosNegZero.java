public class PosNegZero
{
    public static void main(String[] args)
    {
     int a=5;
        if      (a>0)   {   System.out.println(a+" is a Positive number");  }
        else if (a<0)   {   System.out.println(a+" is a Negative number");  }
        else            {   System.out.println(a+" is ZERO");   }
     }
}

