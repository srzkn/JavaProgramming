package day09_IfStatements;

public class GradeReport
{
    public static void main(String[] args) {
int grade = 54;
if      (grade>=85) {   System.out.println("Congrats, you got A");  }
else if (grade>=70) {   System.out.println("Googd, you got B");     }
else if (grade>=55) {   System.out.println("Ok, you got C");        }
else if (grade>=45) {   System.out.println("Ok, you got D");        }
else                {   System.out.println("Oh No! you got E");     }
                                            }
}
